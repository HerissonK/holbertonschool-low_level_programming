/**
 * print_alphabet - Entry point
 *
 * Description: prints "_putchar" followed by a new line using _putchar
 *
 * Return: Always 0 (Success)
 */

void print_to_98(int n)
{
	if (n >= 98)
	{
		while (n > 98)
			printf("%d, ", n--);
		printf("%d\n", n);
	}

	else
	{
		while (n < 98)
			printf("%d, ", n++);
		printf("%d\n", n);
	}
}
